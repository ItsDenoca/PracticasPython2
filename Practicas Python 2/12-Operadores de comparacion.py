a = 4
b = 6

resultado = a == b
print(f"Resultado de comparar == : {resultado}")

resultado = a != b
print(f"Resultado para saber si dos valores son diferentes != : {resultado}")

resultado = a > b
print(f"Resultado de utilizar > : {resultado}")

resultado = a < b
print(f"Resultado de utilizar < : {resultado}")

resultado = a >= b
print(f"Resultado de utilizar >= : {resultado}")

resultado = a <= b
print(f"Resultado de utilizar <= : {resultado}")